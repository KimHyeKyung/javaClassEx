package com.javaex.helloworld;

public class VariableTest {
	public static void main(String[] args) {
		int myAge; //정수형 변수선언
		myAge = 25; //myAge 변수 25로 초기화
		System.out.println(myAge); //myAge값을 출력해라
		
		myAge = 26; //myAge변수의 값을 26으로 초기화
		System.out.println(myAge); //myAge값을 출력해라
	}
}
